/**
 * Deterministically pick today's card based on days since epoch.
 * Returns the same card for the entire calendar day.
 */
export function getTodayCard(): IntentionCard {
  const today = new Date()
  const epoch = Math.floor(today.getTime() / (1000 * 60 * 60 * 24))
  const index = epoch % INTENTION_CARDS.length
  return INTENTION_CARDS[index]
}
