export function getPosesByBodyPart(bodyPartId: string): PoseDef[] {
  return YOGA_POSES.filter(p => p.bodyParts.includes(bodyPartId))
}

// Helper: get pose by id
export function getPoseById(id: string): PoseDef | undefined {
  return YOGA_POSES.find(p => p.id === id)
}

