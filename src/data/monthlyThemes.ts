export function getMilestoneMessage(totalLogs: number): string | null {
  // Check exact milestones
  if (MILESTONE_MESSAGES[totalLogs]) return MILESTONE_MESSAGES[totalLogs]
  return null
}

export function getStreakMessage(streak: number): string | null {
  if (streak === 7) return '🌸 連續七天！你的身體正在悄悄改變。'
  if (streak === 14) return '⭐ 兩週不間斷！這份堅持非常了不起。'
  if (streak === 21) return '💫 三週連續！你已經超越了大多數人。'
  if (streak === 30) return '🎉 連續一個月！你做到了很多人做不到的事。'
  if (streak > 30 && streak % 30 === 0) return `🏆 連續 ${streak} 天！你真的太厲害了。`
  return null
}
