export function getCurrentMonthTheme(): MonthlyTheme {
  const month = new Date().getMonth() + 1 // 1-12
  return MONTHLY_THEMES.find(t => t.month === month) || MONTHLY_THEMES[0]
}

export function getMonthThemeMessage(month: number): string {
  const theme = MONTHLY_THEMES.find(t => t.month === month) || MONTHLY_THEMES[0]
  const msgs = theme.messages
  return msgs[Math.floor(Math.random() * msgs.length)]
}

